// Get brawler name in current language
function getBrawlerName(englishName, lang = null) {
    if (!lang) {
        lang = getCurrentLanguage();
    }
    return brawlerNameTranslations[englishName]?.[lang] || englishName;
}

// Get English name from translated name
function getEnglishName(translatedName) {
    for (const [englishName, translations] of Object.entries(brawlerNameTranslations)) {
        for (const translation of Object.values(translations)) {
            if (translation.toLowerCase() === translatedName.toLowerCase()) {
                return englishName;
            }
        }
    }
    return translatedName;
}
